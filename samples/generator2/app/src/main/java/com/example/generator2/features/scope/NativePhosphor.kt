package com.example.generator2.features.scope

import java.nio.ByteBuffer

object NativePhosphor {

    /** Число бинов по вертикали, должно совпадать с PhosphorGrid::kBins. */
    const val BINS = 512

    init {
        System.loadLibrary("plasma")
    }

    /**
     * Задаёт геометрию сетки и режим отрисовки.
     * @param columns ширина области вывода в пикселях.
     * @param layout 0 — каналы совмещены, 1 — каналы в своих половинах.
     * @param rollMode true при развёртке 32 и выше.
     * @param sweep значение развёртки. Меньше единицы означает часть одного
     * пакета: буфер истории считает пакетами и дробить себя не умеет, поэтому
     * долю отсчитывает уже сетка, забирая хвост нужной длины.
     */
    external fun configure(columns: Int, layout: Int, rollMode: Boolean, sweep: Float)

    /**
     * Возвращает [начальный столбец, количество] изменившихся столбцов,
     * либо null, если выделить массив не удалось.
     */
    external fun update(): IntArray?

    /**
     * Помечает всю сетку грязной, чтобы следующий update() вернул её целиком.
     * Вызывать после пересоздания текстуры: её содержимое потеряно, а
     * конфигурация при этом могла остаться прежней.
     */
    external fun invalidate()

    /**
     * Direct-буфер всей сетки: columns * BINS * 2 half-float.
     *
     * Заливать в текстуру RG16F нужно именно half: на float32 драйвер
     * конвертирует каждый тексель сам и тратит на полную сетку около
     * десяти миллисекунд.
     *
     * Буфер оборачивает нативную память аккумулятора. Любой вызов configure()
     * перевыделяет её, поэтому держать ссылку между вызовами нельзя —
     * получишь обращение к освобождённой памяти. Запрашивать заново после
     * каждого configure().
     */
    external fun gridBuffer(): ByteBuffer?

    /** Смещение чтения текстуры в диапазоне [0, 1). */
    external fun ringOffset(): Float
}
